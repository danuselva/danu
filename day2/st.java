import java.util.*;
public class st {
    public static void main(String[] args) {
        int n;
        int i,j;
        Scanner sc=new Scanner(System.in);
        n=sc.nextInt();
        for(i=0;i<n;i++)
        {
            for(j=0;j<n;j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
    
}
